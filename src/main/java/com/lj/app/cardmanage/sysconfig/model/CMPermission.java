package com.lj.app.cardmanage.sysconfig.model;

import com.lj.app.cardmanage.base.model.BaseModel;

public class CMPermission extends BaseModel{
	private int id;
	private String code;
	private String name;
	private String url;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
